function calcularMediaPorPais(datos, pais) {
  // Filtrar las filas por el país especificado
  const desastresEnPais = datos.filter(dato => dato.applicant_country === pais);

  // Filtrar y convertir los valores de "total_direct_damage_accepted" a números
  const valoresNumericos = desastresEnPais.map(dato => {
    // Eliminar las comas y cualquier otro carácter no numérico y convertir a número
    const valorNumerico = parseFloat(dato.total_direct_damage_accepted.replace(/[^0-9.]/g, ''));
    // Verificar si el valor es un número válido
    return !isNaN(valorNumerico) ? valorNumerico : 0;
  });

  // Calcular la media de los valores numéricos
  const total = valoresNumericos.reduce((acumulador, valor) => acumulador + valor, 0);
  const media = valoresNumericos.length > 0 ? total / valoresNumericos.length : 0;

  return media;
}

